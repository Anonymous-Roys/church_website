        <p>The University of Mines and Technology (UMaT) started as the Tarkwa Technical Institute (TTI) on 3rd November, 1952
        but was officially commissioned by the Government of Ghana, more precisely by His Excellency the Governor of the Gold Coast, 
        Sir Charles Noble Arden Clarke, on 7th October, 1953. In 1961, the Government, upon the advocacy of the Ghana Chamber of Mines, 
        redefined the mission of TTI to incorporate the training of mining technicians and other middle level manpower for the countryâ€™s mining and related industries. 
        In 1961, it was reorganized to become the Tarkwa School of Mines (TSM) to train the required manpower for the mining and allied industries in Ghana. 
        In 1976, the Government took cognizance of the capability of TSM and its strategic location, Tarkwa, which is at the centre of mining activities in the country. 
        As this location gave TSM a special strength for mining education, 
        TSM was affiliated to the Kwame Nkrumah University of Science and Technology (KNUST) as a faculty of the University so as to enable TSM offer degree, 
        diploma and certificate programmes in mining and related fields. The name TSM was changed to KNUST School of Mines (KNUSTSM), Tarkwa. The KNUSTSM, 
        Tarkwa and the Kumasi School of Mines were put together to become the Institute of Mining and Mineral Engineering (IMME). 
        In 1988, a University Rationalization Committee (URC), commissioned by the Ministry of Education,
        recommended the development of the School and its conversion into a University. 
        In 2000, the conversion of KNUSTSM into a University was again strongly recommended by Louis Berger Inc. 
        in association with Kwame Asante and Associates in a report on partial commercialization of KNUST. 
        In 2001, the Council of KNUST considered and approved proposals and 
        recommendations of the Academic Board to merge the School of Mines at Kumasi and the School of Mines at Tarkwa into the Western University College of KNUST, Tarkwa.
        On 3rd November, 2004, at exactly 11.35hrs GMT, 
        the Parliament of the Republic of Ghana passed the bill on the University of Mines and Technology into an Act of Parliament. 
        On 11th November, 2004, the President of Ghana gave the Presidential Assent and on 12th November, 2004 the University of Mines and Technology, 
        Tarkwa Act 2004 (Act 677) was gazetted and thus, became a law. </p>
       
